  zone_id INTEGER NOT NULL,
  barangay VARCHAR(120) NOT NULL,
  purok_count INTEGER NOT NULL DEFAULT 0,
  is_split BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMP NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMP NOT NULL DEFAULT NOW(),
  CONSTRAINT zone_coverage_unique UNIQUE (zone_id, barangay),
  CONSTRAINT zone_coverage_zone_id_fkey
    FOREIGN KEY (zone_id) REFERENCES zone(zone_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
);

CREATE OR REPLACE FUNCTION set_zone_coverage_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_zone_coverage_updated_at ON zone_coverage;
CREATE TRIGGER trg_zone_coverage_updated_at
BEFORE UPDATE ON zone_coverage
FOR EACH ROW
EXECUTE FUNCTION set_zone_coverage_updated_at();

