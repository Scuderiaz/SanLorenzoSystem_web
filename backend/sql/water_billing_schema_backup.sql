-- ---------- Billing ----------
CREATE TABLE IF NOT EXISTS bills (
  bill_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sync_id UUID NOT NULL DEFAULT gen_random_uuid() UNIQUE,
  consumer_id INTEGER NOT NULL,
  reading_id INTEGER NOT NULL UNIQUE,
  billing_officer_id INTEGER,
  billing_month VARCHAR(30),
  date_covered_from TIMESTAMP,
