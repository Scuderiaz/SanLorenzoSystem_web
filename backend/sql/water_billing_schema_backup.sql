CREATE SCHEMA IF NOT EXISTS water_billing;
SET search_path TO water_billing, public;
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- ---------- Core lookup ----------
CREATE TABLE IF NOT EXISTS roles (
  role_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role_name VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS zone (
  zone_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  zone_name VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS classification (
  classification_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  classification_name VARCHAR(50) NOT NULL UNIQUE
);

-- ---------- Accounts ----------
CREATE TABLE IF NOT EXISTS accounts (
  account_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sync_id UUID NOT NULL DEFAULT gen_random_uuid() UNIQUE,
  username VARCHAR(50) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  full_name TEXT,
  email VARCHAR(255),
  contact_number VARCHAR(20),
  profile_picture_url TEXT,
  auth_user_id UUID UNIQUE,
  role_id INTEGER NOT NULL,
  account_status VARCHAR(20) NOT NULL DEFAULT 'Pending',
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  source_site_id VARCHAR(120),
  sync_status VARCHAR(20) NOT NULL DEFAULT 'synced',
  CONSTRAINT accounts_account_status_check
    CHECK (account_status IN ('Pending', 'Approved', 'Rejected', 'Active', 'Inactive')),
  CONSTRAINT fk_accounts_role
    FOREIGN KEY (role_id) REFERENCES roles(role_id)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
);

