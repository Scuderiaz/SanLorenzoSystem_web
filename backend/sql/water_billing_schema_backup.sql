CREATE TABLE IF NOT EXISTS meterreadings (
  reading_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sync_id UUID NOT NULL DEFAULT gen_random_uuid() UNIQUE,
  route_id INTEGER,
  consumer_id INTEGER NOT NULL,
  meter_id INTEGER,
  meter_reader_id INTEGER,
  created_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  reading_status VARCHAR(30) NOT NULL DEFAULT 'Pending',
  previous_reading NUMERIC(12,2) NOT NULL DEFAULT 0,
  current_reading NUMERIC(12,2) NOT NULL DEFAULT 0,
  consumption NUMERIC(12,2) NOT NULL DEFAULT 0,
  excess_consumption NUMERIC(12,2) NOT NULL DEFAULT 0,
  notes TEXT,
  status VARCHAR(20) NOT NULL DEFAULT 'Active',
  reading_date TIMESTAMP NOT NULL,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  source_site_id VARCHAR(120),
  sync_status VARCHAR(20) NOT NULL DEFAULT 'synced',
  CONSTRAINT meterreadings_reading_status_check
    CHECK (reading_status IN ('Pending', 'Normal', 'Recorded', 'Verified', 'Rejected')),
  CONSTRAINT meterreadings_status_check
    CHECK (status IN ('Active', 'Inactive')),
  CONSTRAINT chk_meterreadings_values
    CHECK (
      current_reading >= previous_reading
      AND consumption >= 0
      AND excess_consumption >= 0
    ),
  CONSTRAINT fk_meterreadings_route
    FOREIGN KEY (route_id) REFERENCES route(route_id)
    ON UPDATE CASCADE
    ON DELETE RESTRICT,
  CONSTRAINT fk_meterreadings_consumer
    FOREIGN KEY (consumer_id) REFERENCES consumer(consumer_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_meterreadings_meter
    FOREIGN KEY (meter_id) REFERENCES meter(meter_id)
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  CONSTRAINT fk_meterreadings_reader
    FOREIGN KEY (meter_reader_id) REFERENCES accounts(account_id)
    ON UPDATE CASCADE
    ON DELETE RESTRICT
);

